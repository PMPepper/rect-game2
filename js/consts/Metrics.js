import {C} from './Physics';

//Time Constants
export const MINUTE = 60;
export const HOUR = 3600;
export const DAY = 86400;
export const DAYS_PER_YEAR = 365;//ignore leap years
export const YEAR = DAY * DAYS_PER_YEAR;

//distances
export const LIGHT_SECOND =  C;
export const LIGHT_MINUTE =  C * MINUTE;
export const LIGHT_HOUR =    C * HOUR;
export const LIGHT_DAY =     C * DAY;
export const LIGHT_YEAR =    C * YEAR;
